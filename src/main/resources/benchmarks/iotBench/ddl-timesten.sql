-- IotBench DDL for Timesten
-- FIELDS ARE FIXED TO 100 Bytes

BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE USERTABLE';
EXCEPTION
  WHEN OTHERS THEN
    IF SQLCODE != -942 THEN
      RAISE;
    END IF;
END;

CREATE TABLE USERTABLE (
  IotBench_KEY INT NOT NULL,
  FIELD1 NUMBER,
  FIELD2 NUMBER,
  FIELD3 NUMBER,
  PRIMARY KEY (IotBench_KEY)
);
